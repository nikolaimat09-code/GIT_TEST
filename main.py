# Эта программа получает число от пользователя и выводит его, увеличенное на 1.
# Ввод осуществляется через терминал.

try:
    # Запрашиваем у пользователя число
    num = int(input("Введите число: "))
    # Увеличиваем его на 1 и выводим результат
    print("Результат:", num + 1)
except ValueError:
    # Обрабатываем случай, если введено не число
    print("Ошибка: введите целое число!")
except KeyboardInterrupt:
    # Если пользователь нажал Ctrl+C — выходим без страшной ошибки
    print("\nВвод отменён.")